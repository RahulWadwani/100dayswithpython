# typecasting
# conversion of one data type to another data type is known as typecasting, int(),float(),str(),set(),list(),dict(),hex(),oct()
a = "1"
b = "2"
print(a+b)

# types of typecasting
# explicit conversiont(we do the conversion of any data type to another data type)
print(int(a)+ int(b))

# implict conversion (done by the compiler/python interpreter itself)
c = 1.9
d = 8 
print(c+d)